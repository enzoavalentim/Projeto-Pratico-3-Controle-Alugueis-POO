    private void bntRegistarLocacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntRegistarLocacaoActionPerformed

        int diasDeAluguel = Integer.parseInt(textDiasDeAluguel.getText());
        LocalDate dataLocacao = LocalDate.parse(textDataInicioLocacao.getText(), DateTimeFormatter.ofPattern("dd/MM/yyyy"));
        int idCliente = obterIdClienteSelecionado();
        int idEquipamento = obterIdEquipamentoSelecionado();

        Gerenciamento gerenciarNovoAluguel = new Gerenciamento();
        Alugueis novoAluguel = gerenciarNovoAluguel.cadastrarNovoAluguel(idCliente, idEquipamento, diasDeAluguel, dataLocacao);

        labelClienteNomeId.setText("NOME: " + novoAluguel.getCliente().getNome() + " / ID: " + novoAluguel.getCliente().getId());
        labelProdutoNomeId.setText("EQUIPAMENTO: " + novoAluguel.getProduto().getNome() + " / ID: " + novoAluguel.getProduto().getId());
        labelDataLocacao.setText("DATA DE LOCAÇÃO: " + novoAluguel.getDataLocacao().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
        labelDataDevolucao.setText("DATA DE DEVOLUÇÃO: " + novoAluguel.getDataDevolucao().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
        labelValorTotalLocacao.setText("VALOR TOTAL DA LOCAÇÃO: R$ " + novoAluguel.getVlTotalLocacao());

        JOptionPane.showMessageDialog(null, "ALUGUEL CADASTRADO COM SUCESSO");
        MainFrame n = new MainFrame(); //Inicializa a tela nova
        this.dispose(); //fecha tela que estavamos
        n.setVisible(true); // Mostra  a tela nova
    }//GEN-LAST:event_bntRegistarLocacaoActionPerformed

