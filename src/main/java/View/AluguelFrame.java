    private int obterIdClienteSelecionado() {
        String selecionado = (String) comboBoxClientes.getSelectedItem();
        int idCliente = Integer.parseInt(selecionado.split("/ ID: ")[1].trim());
        return idCliente;
    }

    private int obterIdEquipamentoSelecionado() {
        String selecionado = (String) comboBoxEquipamentos.getSelectedItem();
        int idEquipamento = Integer.parseInt(selecionado.split("/ ID: ")[1].split(" / Valor da Diaria: ")[0].trim());
        return idEquipamento;
    }

